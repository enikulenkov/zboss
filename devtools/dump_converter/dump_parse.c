#include <stdio.h>

static char s_x_tbl[] = "0123456789abcdef";

static void dumpx(int v)
{
  putchar(s_x_tbl[((v) >> 4) & 0xf]);
  putchar(s_x_tbl[((v) & 0xf)]);
  putchar(' ');
}


int
main(
  int argc,
  char **argv)
{
  int c;
  int l;
  int i;
  int n = 1;

  (void)argv;
  (void)argc;

  while ((l = getchar()) != EOF)
  {
    printf("%d\n", n);
    n++;

    dumpx(l);
    for (i = 0 ; i < l-1 && (c = getchar()) != EOF ; ++i)
    {
      if (i % 16 == 0)
      {
        putchar('\n');
      }
      dumpx(c);
    }
    putchar('\n');
    putchar('\n');
  }
}
